                                        <p style="text-align: justify; text-justify: inter-word;">The Automated Water-Level and Rain Monitoring Using Near-Real Time Observation (ALeRTO) is an early warning system developed by the Ateneo de Zamboanga University-GeoSAFER Mindanao ZAMBASULTA Program. ALeRTO Project was funded by the Department of Science and Technology IX through local GIA, being the vice-chair of the Committee on Disaster Prevention and Mitigation of the Regional Disaster Risk Reduction and Management Council IX.</p>
                                        <p style="text-align: justify; text-justify: inter-word;">The system makes use of the Automated Rain Gauges (ARGs) and Automated Water-Level Sensors (AWLS) which were installed in the different river systems in the Region. The device is programmed to monitor the DOST-Advanced Science and Technology Institute (DOST-ASTI) for the critical water level and rain data; and the Philippine Atmospheric, Geophysical and Astronomical Services Administration (PAGASA) for the status of the hydro-meteorological data in the region. The device determines the threshold category in each river, which is represented by the different color warnings. Once the device determines that a particular river has reached a certain threshold, it will automatically send out alert messages via SMS to registered users, specifically to Local Government Units. The SMS will contain the threshold interpretation, along with the rain and water level values.</p>
